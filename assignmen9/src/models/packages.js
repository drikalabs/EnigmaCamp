class Packages {
    constructor(idPackage,ownerPackage,packageFrom,packageDestination,weight,price){
        this.idPackage = idPackage;
        this.ownerPackage = ownerPackage;
        this.packageFrom = packageFrom;
        this.packageDestination = packageDestination;
        this.weight = weight;
        this.price = price;
    }
}
export default Packages;